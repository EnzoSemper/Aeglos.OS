/// 8x8 bitmap font for framebuffer text rendering.
///
/// Covers printable ASCII (32..=126). Each glyph is 8 bytes — one byte
/// per row, MSB is the leftmost pixel. Based on the classic VGA 8x8 style.

pub const GLYPH_WIDTH: usize = 8;
pub const GLYPH_HEIGHT: usize = 8;

const FIRST_CHAR: u8 = 32;
const LAST_CHAR: u8 = 126;

/// Unknown-character glyph (filled box).
const UNKNOWN: [u8; 8] = [0xFF, 0x81, 0xBD, 0xA5, 0xA5, 0xBD, 0x81, 0xFF];

/// Return the 8-byte glyph bitmap for a given ASCII character.
pub fn glyph(ch: u8) -> &'static [u8; 8] {
    if ch >= FIRST_CHAR && ch <= LAST_CHAR {
        &FONT_DATA[(ch - FIRST_CHAR) as usize]
    } else {
        &UNKNOWN
    }
}

